public class Color {
    private final String GRAY = "GRAY";
    private final String BLACK = "BLACK";



    public String[] getGrey() {
        String[] color = {GRAY};
        return color;
    }

    public String[] getBlack() {
        String[] color = {BLACK};
        return color;
    }

    public String[] getBlackAndGray() {
        String[] color = {BLACK, GRAY};
        return color;
    }

}
